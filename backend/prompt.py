def build_user_prompt(
    pain_points: list[str],
    custom_pain_point: str | None,
    story: str,
    main_question: str,
) -> str:
    points = pain_points.copy()
    if custom_pain_point:
        points.append(custom_pain_point)
    pain_str = "、".join(points) if points else "未指定"

    selected_label = QUESTION_LABELS.get(main_question, main_question)
    selected_scope = PERSPECTIVE_SCOPES.get(main_question, "")

    other_ids = [k for k in QUESTION_LABELS if k != main_question]
    other_scopes = "\n\n".join(PERSPECTIVE_SCOPES[k] for k in other_ids)

    return f"""她描述的最让她难受的地方：{pain_str}

最近最让她难受的一件事：
{story}

---

她最想知道的问题：{selected_label}

selected_question_answer 必须严格遵守以下边界和要求：
{selected_scope}

---

other_perspectives 必须覆盖以下4个问题，每个严格遵守各自边界和要求：

{other_scopes}

---

重要：
1. 所有模块必须基于同一底层关系判断，不能互相矛盾。
2. 每个模块只从自己的角度切入，禁止重复其他模块的内容和表达。
3. 每个模块必须包含「用户理解层」——帮用户理解自己，不只是分析对方。
4. 语气平静，不要连续重锤，不要判决感。"""
